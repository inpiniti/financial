/**
 * 05. 앙드레 코스톨라니 — 돈, 뜨겁게 사랑하고 차갑게 다루어라
 * "2 더하기 2는 증권거래소에서 5가 되기도 하고 3이 되기도 한다"
 * ※ _공통.js를 먼저 붙여넣을 것
 */
await tossScreen("05 앙드레 코스톨라니", [
  F("시가총액", 1 * 조, null),                                           // 대형주 (1조원 이상)
  FP("부채_비율", 기간_분기TTM("TTM"), null, 1, { includeTo: true }),     // 100% 이하 ("빚진 자는 소신파가 못 된다")
  FP("이자_보상_배율", 기간_분기TTM("TTM"), 5, null),                     // 500% 이상
  FP("ROE", 기간_분기TTM("TTM"), 0.1, null),                             // 10% 이상 (유능한 경영진의 대용품)
  F("PER", 0, 15, { includeTo: true }),                                  // 0~15배
  FP("영업_이익_연속_증가", 기간_분기TTM("TTM"), 3, null),                 // 연도별 3년 이상 연속
]);

// 선택 추가: 배당 연속지급 5년↑ / 유동비율 100%↑ / 배당수익률 3%↑
// 너무 많으면: PER 15→10배, ROE 10→15%
// 너무 적으면: 이자보상배율 500→300%, 영업이익 연속증가 3→2년
