/**
 * 09. 뉴욕주민 — 사업을 사는 관점 + 월가식 데이터 회의주의
 * ⭐ 핵심: "증감률"이 아니라 "연속증가"를 써라.
 *    순이익 증감률(QoQ/TTM)은 자산매각·환차익 같은 일회성 손익 하나로도 수십 % 튄다.
 * ※ _공통.js를 먼저 붙여넣을 것
 */
await tossScreen("09 뉴욕주민", [
  F("시가총액", 3000 * 억, null),                                        // 3,000억원 이상 (공시 신뢰도)
  FP("부채_비율", 기간_분기TTM("TTM"), null, 1, { includeTo: true }),      // 100% 이하
  FP("이자_보상_배율", 기간_분기TTM("TTM"), 3, null),                      // 300% 이상 ← 좀비기업 배제 장치
  FP("영업_이익률", 기간_분기TTM("TTM"), 0.1, null),                       // 10% 이상 (진입장벽 대리지표)
  FP("ROE", 기간_분기TTM("TTM"), 0.1, null),                              // 10% 이상 (현금창출력)
  FP("순이익_연속_증가", 기간_분기TTM("TTM"), 3, null),                    // 연도별 3년 ← 일회성 손익 왜곡 방어
  F("PER", 0, 20, { includeTo: true }),                                  // 0~20배
]);

// 선택 추가: 배당 연속지급 3년↑ / 거래대금 50억↑ / 매출액 연속증가 2년↑
// 너무 많으면: PER 20→15배 또는 시가총액을 대형주(1조↑)로
// 너무 적으면: 이자보상배율 제거 또는 순이익 연속증가 3→2년
//   부채비율·ROE·영업이익률은 "사업의 질" 핵심 3종이니 마지막까지 남겨둬라
