/**
 * 11. 애스워스 다모다란 — 가치 함정(value trap) 방벽을 두른 저평가 스크리닝
 * ⭐ 핵심: PBR의 동인은 ROE다. ROE가 자기자본비용(국내 8~10%) 밑이면
 *         PBR 1배 미만은 저평가가 아니라 "정당한 가격"이다.
 *         → ROE·이자보상배율·부채비율 없이 PER·PBR만 조이면 가치 함정이 대량으로 걸린다.
 * ※ _공통.js를 먼저 붙여넣을 것
 */
await tossScreen("11 애스워스 다모다란", [
  F("시가총액", 3000 * 억, null),                                        // 3,000억원 이상
  F("주가", 1000, null, { includeFrom: false }),                         // 동전주 제외
  F("PER", 0, 15, { includeTo: true }),                                  // 0~15배 (1차 가격 필터)
  F("PBR", 0, 1.5, { includeTo: true }),                                 // 0~1.5배
  FP("ROE", 기간_분기TTM("TTM"), 0.1, null),                             // 10% 이상 ← 가치 함정 방벽 (핵심)
  FP("영업_이익률", 기간_분기TTM("TTM"), 0.1, null),                      // 10% 이상
  FP("이자_보상_배율", 기간_분기TTM("TTM"), 3, null),                     // 300% 이상 (생존 확률)
  FP("부채_비율", 기간_분기TTM("TTM"), null, 2, { includeTo: true }),      // 200% 이하
  FP("연평균_순이익_증감률", 기간_3년5년("TTM_3"), 0.1, null),            // 최근 3년 평균 10% 이상
]);

// 너무 많으면: PER 15→12배, ROE 10→12%부터 조여라
// 너무 적으면: PBR 1.5→2배를 먼저 풀어라.
//   이자보상배율·부채비율(생존 게이트)은 가장 나중에 — 위험을 눈감아주는 셈이다
