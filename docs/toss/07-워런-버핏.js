/**
 * 07. 워런 버핏 — 해자(moat)의 흔적을 숫자에서 찾는다
 * "재무제표는 숫자의 나열이 아니라 기업의 경쟁우위가 기록된 성적표다."
 * ※ _공통.js를 먼저 붙여넣을 것
 */
await tossScreen("07 워런 버핏", [
  F("시가총액", 3000 * 억, null),                                        // 3,000억원 이상
  FP("매출_총_이익률", 기간_분기TTM("TTM"), 0.4, null),                   // 40% 이상 ← 해자의 첫 번째 신호
  FP("ROE", 기간_분기TTM("TTM"), 0.15, null),                            // 15% 이상
  FP("순_이익률", 기간_분기TTM("TTM"), 0.15, null),                       // 15% 이상 (지속적 경쟁우위의 증거)
  FP("부채_비율", 기간_분기TTM("TTM"), null, 1, { includeTo: true }),      // 100% 이하 ← ROE가 레버리지가 아닌 영업효율에서 나오는지 확인
  FP("순이익_연속_증가", 기간_분기TTM("TTM"), 3, null),                    // 연도별 3년 이상 연속
]);

// 선택 추가: 이자보상배율 300%↑ / 매출총이익 연속증가 3년↑ / 영업이익률 15%↑ / PER 0~25배
// 너무 많으면: 매출총이익률 40→50%, 순이익 연속증가 3→4년
// 너무 적으면: 시가총액 3,000억→1,000억, 부채비율 100→150%
//   ROE·순이익률은 해자를 가늠하는 핵심이니 가장 나중에 건드려라
