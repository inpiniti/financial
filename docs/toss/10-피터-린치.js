/**
 * 10. 피터 린치 — PEG 근사 (PER ÷ 이익성장률 ≤ 1)
 * PER 20배 ÷ 연평균 순이익 성장률 20% = PEG 1.0  ← 린치의 마지노선
 * (더 깐깐하게: PER 상한을 15배로 낮추면 PEG ≈ 0.75)
 * ※ _공통.js를 먼저 붙여넣을 것
 */
await tossScreen("10 피터 린치", [
  F("시가총액", 1000 * 억, null),                                        // 1,000억원 이상
  F("PER", 0, 20),                                                       // PEG의 분자 (적자기업 자동 배제)
  FP("연평균_순이익_증감률", 기간_3년5년("TTM_3"), 0.2, null),            // PEG의 분모 (최근 3년 평균 20% 이상)
  FP("부채_비율", 기간_분기TTM("TTM"), null, 1, { includeTo: true }),      // 100% 이하
  FP("영업_이익률", 기간_분기TTM("TTM"), 0.1, null),                       // 10% 이상 (틈새독점의 흔적)
  FP("거래대금", 기간_일년("DAY_1"), 5 * 억, null),                        // 5억원 이상 (유동성 최소선)
]);

